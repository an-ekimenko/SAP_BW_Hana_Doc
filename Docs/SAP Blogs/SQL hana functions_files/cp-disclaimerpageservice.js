	disclaimerService.skipDisclaimer = function(url){
		
		var absoluteUrl, index = "/viewer/disclaimer-for-links?q=".length;

		absoluteUrl = url;
		url = absoluteUrl.substring(index);
		
		var decodedURL = disclaimerService.extractHostname(decodeURIComponent(url));
		var decodedRootURL = disclaimerService.extractRootDomain(decodeURIComponent(url));
		if (disclaimerService.isWhiteListUrl(decodedURL)==true || disclaimerService.isSAPSubdomain(decodedURL)==true ){
				
			window.open(decodeURIComponent(url));
		
		}else if (disclaimerService.isWhiteListUrl(decodedRootURL)==true || disclaimerService.isSAPSubdomain(decodedRootURL)==true ){
			
			window.open(decodeURIComponent(url));
			
		}else{
			
			window.open("/viewer/page-cannot-be-accessed");	
		 
		}
	}

	
	
	disclaimerService.isWhiteListUrl = function(link){

		if(links.hasOwnProperty(link)){
			return true;
		} else {
			return false;
		}
	}

	disclaimerService.isSAPSubdomain = function(link){
		var sapCheck = new RegExp('\\bsap\\b');
		var comCheck = new RegExp('\\bcom\\b');
		if(sapCheck.test(link) && comCheck.test(link)){
			return true;
		} 
		return false;
	}
	
	disclaimerService.extractHostname = function (link) {
	    var hostname;
	    //find & remove protocol (http, ftp, etc.) and get hostname

	    if (link.indexOf("://") > -1) {
	        hostname = link.split('/')[2];
	    }
	    else {
	        hostname = link.split('/')[0];
	    }

	    //find & remove port number
	    hostname = hostname.split(':')[0];
	    //find & remove "?"
	    hostname = hostname.split('?')[0];
	    var splitHost = hostname.split('.');
	    if(splitHost[0] == 'www'){
	    	
	    	hostname = hostname.split ('www.')[1];
	    }
	    
	    return hostname;
	}
	
	disclaimerService.extractRootDomain = function (link) {
	    var domain = disclaimerService.extractHostname(link),
	        splitArr = domain.split('.'),
	        arrLen = splitArr.length;

	    //extracting the root domain here
	    if (arrLen > 2) {
	        domain = splitArr[arrLen - 2] + '.' + splitArr[arrLen - 1];
	    }
	    return domain;
	}
	
	

	
	
	
